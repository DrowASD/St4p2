def power_and_multiply(num):
    # Извлекаем цифры
    сотни = num // 100
    десятки = (num % 100) // 10
    единицы = num % 10

    # Рассчитываем результат
    результат = (десятки ** единицы) * сотни
    результат /= (десятки * 10 - единицы)

    return результат

# Пример использования
num = 46275
print(power_and_multiply(num))  # Вывод: -16807.0

